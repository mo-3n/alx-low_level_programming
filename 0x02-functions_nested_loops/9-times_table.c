#include "main.h"

/**
 * times_table - This function do something.
 *
 * Argument description.
 * Return value description.
 */
void times_table(void)
{
	int x, y;

	for (x = 0; x < 10; x++)
	{
		int m = x * y;
		for (y = 0; y < 10; y++)
		{
			_putchar('m');
			_putchar(',');
			_putchar(' ');
		}
		_putchar('\n');
	}
}
